# Main Experiment
# ============================================================

def run_experiment():
    print("=" * 60)
    print("EXPERIMENT 1: Synthetic PID Control Task")
    print("=" * 60)
    
    # --- Config ---
    D_INPUT = 4
    D_HIDDEN = 64
    D_OUTPUT = 1
    SEQ_LEN = 64
    N_TRAIN = 4000
    N_VAL = 1000
    BATCH_SIZE = 64
    EPOCHS = 100
    LR = 3e-3
    
    # --- Generate Data ---
    print("\n📊 Generating PID controller data...")
    X_train, Y_train, true_gains = generate_pid_data(
        n_samples=N_TRAIN, seq_len=SEQ_LEN, d_input=D_INPUT, seed=42
    )
    X_val, Y_val, _ = generate_pid_data(
        n_samples=N_VAL, seq_len=SEQ_LEN, d_input=D_INPUT, seed=123
    )
    
    train_loader = DataLoader(
        TensorDataset(X_train, Y_train), 
        batch_size=BATCH_SIZE, shuffle=True
    )
    val_loader = DataLoader(
        TensorDataset(X_val, Y_val),
        batch_size=BATCH_SIZE
    )
    
    print(f"  Train: {X_train.shape} → {Y_train.shape}")
    print(f"  Val:   {X_val.shape} → {Y_val.shape}")
    print(f"  Signal range: [{X_train.min():.2f}, {X_train.max():.2f}]")
    print(f"  Target range: [{Y_train.min():.2f}, {Y_train.max():.2f}]")
    
    # --- Models ---
    models = {
        "MLP": MLPBaseline(D_INPUT, D_HIDDEN, D_OUTPUT, n_layers=2),
        "LSTM": LSTMBaseline(D_INPUT, D_HIDDEN, D_OUTPUT, n_layers=2),
        "PID-Net": PIDNet(
            d_input=D_INPUT, d_model=D_HIDDEN, d_output=D_OUTPUT,
            n_layers=2, gate_type="softmax"
        ),
    }
    
    print("\n📐 Model sizes:")
    for name, model in models.items():
        params = sum(p.numel() for p in model.parameters())
        print(f"  {name}: {params:,} params")
    
    # --- Train ---
    results = {}
    for name, model in models.items():
        print(f"\n🏋️ Training {name}...")
        sparsity_w = 0.01 if name == "PID-Net" else 0.0
        history = train_model(
            model, train_loader, val_loader,
            epochs=EPOCHS, lr=LR,
            sparsity_weight=sparsity_w,
            model_name=name,
        )
        results[name] = history
    
    # --- Results ---
    print("\n" + "=" * 60)
    print("RESULTS")
    print("=" * 60)
    
    print(f"\n{'Model':<12} {'Params':>10} {'Best Val MSE':>14} {'Epochs to 0.01':>16} {'Avg Time/Epoch':>16}")
    print("-" * 70)
    
    for name, hist in results.items():
        epochs_str = str(hist['epochs_to_target']) if hist['epochs_to_target'] else "N/A"
        avg_time = np.mean(hist['epoch_time'])
        print(f"{name:<12} {hist['total_params']:>10,} {hist['best_val']:>14.6f} {epochs_str:>16} {avg_time:>14.3f}s")
    
    # --- Gate Analysis (PID-Net only) ---
    pid_model = models["PID-Net"]
    analyze_gates(pid_model, X_val[:32], true_gains)
    
    # --- Save results ---
    output_dir = Path("results")
    output_dir.mkdir(exist_ok=True)
    
    serializable = {}
    for name, hist in results.items():
        serializable[name] = {
            "best_val": hist["best_val"],
            "epochs_to_target": hist["epochs_to_target"],
            "total_params": hist["total_params"],
            "final_train_loss": hist["train_loss"][-1],
            "final_val_loss": hist["val_loss"][-1],
        }
    
    with open(output_dir / "experiment_1_results.json", "w") as f:
        json.dump(serializable, f, indent=2)
    
    print(f"\n✅ Results saved to {output_dir / 'experiment_1_results.json'}")
    
    # --- Verdict ---
    print("\n" + "=" * 60)
    print("VERDICT")
    print("=" * 60)
    
    pid_val = results["PID-Net"]["best_val"]
    mlp_val = results["MLP"]["best_val"]
    lstm_val = results["LSTM"]["best_val"]
    
    if pid_val < mlp_val and pid_val < lstm_val:
        improvement_mlp = (1 - pid_val / mlp_val) * 100
        improvement_lstm = (1 - pid_val / lstm_val) * 100
        print(f"🏆 PID-Net WINS!")
        print(f"   vs MLP:  {improvement_mlp:.1f}% lower MSE")
        print(f"   vs LSTM: {improvement_lstm:.1f}% lower MSE")
    elif pid_val < mlp_val:
        print(f"✅ PID-Net beats MLP, but LSTM is better")
        print(f"   PID: {pid_val:.6f} vs LSTM: {lstm_val:.6f}")
    else:
        print(f"⚠️ PID-Net didn't win this round")
        print(f"   PID: {pid_val:.6f} | MLP: {mlp_val:.6f} | LSTM: {lstm_val:.6f}")
        print(f"   May need tuning or more epochs")
    
    return results


if __name__ == "__main__":
    results = run_experiment()
