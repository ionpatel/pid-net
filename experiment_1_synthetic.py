"""
Experiment 1: Synthetic PID Control Task

Goal: Verify that PID-Net can recover known P/I/D gains from data
generated by actual PID controllers.

We generate input signals, pass them through PID controllers with known gains,
then train PID-Net vs MLP vs LSTM to predict the controller output.

Success criteria:
1. PID-Net achieves lower MSE than MLP and LSTM
2. PID-Net gate activations correlate with the true P/I/D gain magnitudes
3. PID-Net converges faster (fewer epochs to target MSE)
"""

import torch
import torch.nn as nn
import torch.optim as optim
from torch.utils.data import DataLoader, TensorDataset
import numpy as np
import time
import json
from pathlib import Path
from pid_layer import PIDLinear, PIDNet, pid_sparsity_loss


# ============================================================
# Data Generation: True PID Controllers
# ============================================================

def generate_pid_data(
    n_samples: int = 5000,
    seq_len: int = 64,
    d_input: int = 4,
    pid_gains: dict = None,
    noise_std: float = 0.01,
    seed: int = 42,
):
    """
    Generate data from a multi-input PID controller.
    
    Each input channel has its own P/I/D gains.
    Output = sum over channels of (Kp*x + Ki*integral(x) + Kd*derivative(x))
    
    Returns:
        X: (n_samples, seq_len, d_input)
        Y: (n_samples, seq_len, 1)
        gains: dict of true gains per channel
    """
    rng = np.random.RandomState(seed)
    
    if pid_gains is None:
        # Default: varying gains per channel to test recovery
        pid_gains = {
            0: {"Kp": 1.0, "Ki": 0.5, "Kd": 0.1},   # P-dominant
            1: {"Kp": 0.1, "Ki": 1.0, "Kd": 0.2},   # I-dominant
            2: {"Kp": 0.2, "Ki": 0.1, "Kd": 1.0},   # D-dominant
            3: {"Kp": 0.5, "Ki": 0.5, "Kd": 0.5},   # Balanced
        }
    
    # Generate diverse input signals
    X = np.zeros((n_samples, seq_len, d_input), dtype=np.float32)
    
    for i in range(n_samples):
        signal_type = i % 5
        for ch in range(d_input):
            t = np.linspace(0, 4 * np.pi, seq_len)
            
            if signal_type == 0:
                # Sinusoidal
                freq = rng.uniform(0.5, 3.0)
                X[i, :, ch] = np.sin(freq * t + rng.uniform(0, 2*np.pi))
            elif signal_type == 1:
                # Step function with noise
                step_pos = rng.randint(seq_len // 4, 3 * seq_len // 4)
                X[i, step_pos:, ch] = rng.uniform(0.5, 2.0)
                X[i, :, ch] += rng.randn(seq_len) * 0.05
            elif signal_type == 2:
                # Ramp
                slope = rng.uniform(-1, 1)
                X[i, :, ch] = slope * np.linspace(0, 1, seq_len)
            elif signal_type == 3:
                # Random walk
                X[i, :, ch] = np.cumsum(rng.randn(seq_len) * 0.1)
            else:
                # Mixed: sin + trend + noise
                X[i, :, ch] = (
                    np.sin(rng.uniform(1, 3) * t) * 0.5 +
                    np.linspace(0, rng.uniform(-1, 1), seq_len) +
                    rng.randn(seq_len) * 0.1
                )
    
    # Apply true PID controller
    Y = np.zeros((n_samples, seq_len, 1), dtype=np.float32)
    alpha_true = 0.9  # EMA decay for integral
    
    for ch in range(d_input):
        Kp = pid_gains[ch]["Kp"]
        Ki = pid_gains[ch]["Ki"]
        Kd = pid_gains[ch]["Kd"]
        
        x = X[:, :, ch]  # (n_samples, seq_len)
        
        # Proportional term
        p_term = Kp * x
        
        # Integral term (EMA)
        integral = np.zeros_like(x)
        for t in range(1, seq_len):
            integral[:, t] = alpha_true * integral[:, t-1] + (1 - alpha_true) * x[:, t]
        i_term = Ki * integral
        
        # Derivative term
        derivative = np.zeros_like(x)
        derivative[:, 1:] = x[:, 1:] - x[:, :-1]
        d_term = Kd * derivative
        
        Y[:, :, 0] += p_term + i_term + d_term
    
    # Add observation noise
    Y += rng.randn(*Y.shape).astype(np.float32) * noise_std
    
    return (
        torch.from_numpy(X),
        torch.from_numpy(Y),
        pid_gains,
    )


# ============================================================
# Baseline Models
# ============================================================

class MLPBaseline(nn.Module):
    """Standard MLP applied per-timestep (no temporal awareness)."""
    
    def __init__(self, d_input, d_hidden, d_output, n_layers=2):
        super().__init__()
        layers = [nn.Linear(d_input, d_hidden), nn.GELU()]
        for _ in range(n_layers - 1):
            layers.extend([nn.Linear(d_hidden, d_hidden), nn.GELU()])
        layers.append(nn.Linear(d_hidden, d_output))
        self.net = nn.Sequential(*layers)
    
    def forward(self, x):
        return self.net(x)


class LSTMBaseline(nn.Module):
    """LSTM baseline with temporal awareness."""
    
    def __init__(self, d_input, d_hidden, d_output, n_layers=2):
        super().__init__()
        self.lstm = nn.LSTM(d_input, d_hidden, n_layers, batch_first=True)
        self.proj = nn.Linear(d_hidden, d_output)
    
    def forward(self, x):
        h, _ = self.lstm(x)
        return self.proj(h)


# ============================================================
# Training
# ============================================================

def train_model(model, train_loader, val_loader, epochs=100, lr=1e-3, 
                sparsity_weight=0.0, model_name="model"):
    """Train a model and return metrics."""
    optimizer = optim.AdamW(model.parameters(), lr=lr, weight_decay=1e-4)
    scheduler = optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=epochs)
    criterion = nn.MSELoss()
    
    history = {
        "train_loss": [],
        "val_loss": [],
        "epoch_time": [],
    }
    
    best_val = float('inf')
    epochs_to_target = None
    target_mse = 0.01  # convergence target
    
    for epoch in range(epochs):
        # Train
        model.train()
        train_loss = 0
        t0 = time.time()
        
        for X_batch, Y_batch in train_loader:
            optimizer.zero_grad()
            pred = model(X_batch)
            loss = criterion(pred, Y_batch)
            
            # Add sparsity loss for PID-Net
            if sparsity_weight > 0:
                loss = loss + sparsity_weight * pid_sparsity_loss(model)
            
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            optimizer.step()
            train_loss += loss.item()
        
        scheduler.step()
        train_loss /= len(train_loader)
        epoch_time = time.time() - t0
        
        # Validate
        model.eval()
        val_loss = 0
        with torch.no_grad():
            for X_batch, Y_batch in val_loader:
                pred = model(X_batch)
                val_loss += criterion(pred, Y_batch).item()
        val_loss /= len(val_loader)
        
        history["train_loss"].append(train_loss)
        history["val_loss"].append(val_loss)
        history["epoch_time"].append(epoch_time)
        
        if val_loss < best_val:
            best_val = val_loss
        
        if val_loss < target_mse and epochs_to_target is None:
            epochs_to_target = epoch + 1
        
        if (epoch + 1) % 20 == 0 or epoch == 0:
            print(f"  [{model_name}] Epoch {epoch+1:3d} | "
                  f"Train: {train_loss:.6f} | Val: {val_loss:.6f} | "
                  f"Time: {epoch_time:.2f}s")
    
    history["best_val"] = best_val
    history["epochs_to_target"] = epochs_to_target
    history["total_params"] = sum(p.numel() for p in model.parameters())
    
    return history


# ============================================================
# Gate Analysis
# ============================================================

def analyze_gates(model, X_sample, true_gains):
    """
    Analyze PID-Net gate activations and compare to true gains.
    
    If the architecture works, gate activations should correlate
    with the magnitude of the true P/I/D gains.
    """
    model.eval()
    
    print("\n" + "=" * 60)
    print("GATE ANALYSIS — Do gates match true PID gains?")
    print("=" * 60)
    
    print("\nTrue gains per channel:")
    for ch, gains in true_gains.items():
        total = gains["Kp"] + gains["Ki"] + gains["Kd"]
        print(f"  Ch {ch}: P={gains['Kp']:.1f} ({gains['Kp']/total:.1%}) | "
              f"I={gains['Ki']:.1f} ({gains['Ki']/total:.1%}) | "
              f"D={gains['Kd']:.1f} ({gains['Kd']/total:.1%})")
    
    with torch.no_grad():
        # Run through first PID layer and get gates
        for name, module in model.named_modules():
            if isinstance(module, PIDLinear):
                _, gates = module(X_sample if 'input' in name or 'pid1' in name 
                                  else model.input_proj(X_sample), 
                                  return_gates=True)
                stats = module.get_gate_stats()
                print(f"\n  Layer [{name}]:")
                print(f"    Gate P: {stats['gate_p_mean']:.3f} ± {stats['gate_p_std']:.3f}")
                print(f"    Gate I: {stats['gate_i_mean']:.3f} ± {stats['gate_i_std']:.3f}")
                print(f"    Gate D: {stats['gate_d_mean']:.3f} ± {stats['gate_d_std']:.3f}")
                break  # Just analyze first layer
    
    # Check alpha (integral decay) learned value
    for name, module in model.named_modules():
        if isinstance(module, PIDLinear):
            alpha_mean = module.alpha.mean().item()
            beta_mean = module.beta.mean().item()
            print(f"\n  Learned α (integral decay): {alpha_mean:.4f} (true: 0.9)")
            print(f"  Learned β (deriv smoothing): {beta_mean:.4f}")
            break


